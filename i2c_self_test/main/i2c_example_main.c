/*______________________
 * | start | slave_addr + wr_bit  | write n bytes +   | stop |
 *
 */
static esp_err_t i2c_master_write_comm(i2c_port_t i2c_num, uint8_t data_wr, size_t size)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (I2C_ADDR << 1) | I2C_MASTER_WRITE, ACK_CHECK_DIS);
    i2c_master_write_byte(cmd, COMM_MODE, ACK_CHECK_DIS);
    i2c_master_write_byte(cmd, data_wr, ACK_CHECK_DIS);
    i2c_master_stop(cmd);
    esp_err_t ret = i2c_master_cmd_begin(i2c_num, cmd, 10);
    i2c_cmd_link_delete(cmd);
    return ret;
}

static esp_err_t i2c_master_write_data(i2c_port_t i2c_num, uint8_t *data, size_t size)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (I2C_ADDR << 1) | I2C_MASTER_WRITE, ACK_CHECK_DIS);
    i2c_master_write_byte(cmd, DATA_MODE, ACK_CHECK_DIS);
    i2c_master_write(cmd, data, size, ACK_CHECK_DIS);
    i2c_master_stop(cmd);
    esp_err_t ret = i2c_master_cmd_begin(i2c_num, cmd, 10);
    i2c_cmd_link_delete(cmd);
    return ret;
}

